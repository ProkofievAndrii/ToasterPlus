import Foundation

/// Predefined delay and duration values for displaying toasts.
///
/// These values provide convenient constants for controlling the
/// timing of toast visibility.
public enum Delay {
    // MARK: - Preset Values

    /// A short delay or duration (2 seconds).
    public static let short: TimeInterval = 2.0

    /// A long delay or duration (3.5 seconds).
    public static let long: TimeInterval = 3.5
}


